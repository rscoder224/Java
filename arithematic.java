import java.lang.*;

// Arithematic Oprators In java
// + - * / %;

public class arithematic{
    public static void main(String[] args){
        int a = 35;
        int b = 5;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
        
        double x = 1.89;
        double y = 2.76;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);
        System.out.println(x%y);
    }
}
